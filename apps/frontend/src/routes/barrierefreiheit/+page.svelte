<section class="mx-auto max-w-3xl px-4 py-8">
	<h1 class="text-3xl font-bold tracking-tight text-slate-900">Erklärung zur Barrierefreiheit</h1>

	<p class="mt-4 text-slate-600">
		worldweathernews.com ist bemüht, die Plattform im Einklang mit dem
		Barrierefreiheitsstärkungsgesetz (BFSG, in Kraft seit 28. Juni 2025) und den darauf basierenden
		Anforderungen der EN 301 549 sowie WCAG 2.1 Level AA barrierefrei zugänglich zu machen.
	</p>

	<h2 class="mt-8 text-xl font-semibold text-slate-900">
		Stand der Vereinbarkeit mit den Anforderungen
	</h2>
	<p class="mt-3 text-slate-600">
		<!-- TODO Maintainer: Nach erstem Audit (Lighthouse + manuelle Stichprobe) Konformitäts-Stand verbindlich festlegen. Vorschlag bis dahin: "teilweise konform". Bei vollständiger Konformität dann auf "vollständig konform" anheben. -->
		Diese Plattform ist <strong class="text-slate-900">teilweise konform</strong> mit den Anforderungen
		der WCAG 2.1 Level AA. Die nachfolgend aufgeführten Inhalte sind aus den angegebenen Gründen nicht
		barrierefrei.
	</p>

	<h2 class="mt-8 text-xl font-semibold text-slate-900">Nicht barrierefreie Inhalte</h2>
	<p class="mt-3 text-slate-600">
		Da sich die Plattform in der Forschungs-Phase befindet und noch wenige Funktionen produktiv
		sind, ist eine vollständige Konformitätsprüfung noch nicht erfolgt. Bekannte Einschränkungen und
		geplante Verbesserungen:
	</p>
	<ul class="mt-2 list-disc space-y-1 pl-6 text-slate-600">
		<li>Eine umfassende Tastatur-Bedienbarkeitsprüfung steht aus (geplant Iteration 1.2).</li>
		<li>Kontrastverhältnisse einzelner UI-Elemente sind noch nicht systematisch validiert.</li>
		<li>
			Wetterkarten und Diagramme (in späteren Iterationen) werden zusätzliche Text-Alternativen
			benötigen.
		</li>
		<li>Eine Sprach-Auswahl (Deutsch/Englisch) folgt mit der i18n-Integration in Iteration 1.2.</li>
	</ul>

