<section class="mx-auto max-w-3xl px-4 py-8">
	<h1 class="text-3xl font-bold tracking-tight text-slate-900">Über worldweathernews.com</h1>

	<p class="mt-4 text-slate-600">
		worldweathernews.com ist eine Plattform für Wetter- und Klimainformationen, die Daten aus
		nationalen Wetterdiensten weltweit aggregiert, meteorologisch einordnet und einer Community von
		Beobachtern, Citizen Scientists und Wetter-Interessierten zugänglich macht.
	</p>

	<h2 class="mt-8 text-xl font-semibold text-slate-900">Mission</h2>
	<p class="mt-3 text-slate-600">
		Wetterdaten sind weltweit verfügbar, aber für Laien schwer zu finden, schwer zu vergleichen und
		schwer einzuordnen. Wir machen die Daten der nationalen Wetterdienste durchsuchbar, ergänzen sie
		um meteorologischen Kontext und erlauben es einer Community, eigene Beobachtungen beizutragen.
	</p>

	<h2 class="mt-8 text-xl font-semibold text-slate-900">Maintainer</h2>
	<p class="mt-3 text-slate-600">
		Die Plattform wird betrieben von <strong class="text-slate-900">Heinz W. Richter</strong>,
		Diplom-Meteorologe und IT-Architekt. Hosting läuft auf eigener Infrastruktur (Proxmox-Cluster in
		Deutschland) — keine Public-Cloud-Abhängigkeiten im kritischen Pfad.
	</p>

	<h2 class="mt-8 text-xl font-semibold text-slate-900">Forschungs-Phase</h2>
	<p class="mt-3 text-slate-600">
		Aktuell ist worldweathernews.com in der Forschungs-Phase. Das bedeutet konkret: kein
		SLA-Versprechen, keine garantierte Datenaktualität, kein automatisches Failover. Die
		Infrastruktur ist auf Lerneffekt und Kostenkontrolle optimiert, nicht auf Hochverfügbarkeit.
		Sobald die Plattform Feature-reif ist, wird sie auf eine echte Produktionsumgebung migriert.
	</p>

	<h2 class="mt-8 text-xl font-semibold text-slate-900">Open Source</h2>
	<p class="mt-3 text-slate-600">
		Der Code ist unter der AGPL-3.0 lizenziert. Repository und Issue-Tracker sind öffentlich
		einsehbar — Beiträge sind willkommen.
	</p>
</section>
